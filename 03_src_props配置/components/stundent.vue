<template>
  <div>
    <h1>{{msg}}</h1>
    <h2>学生姓名：{{name}}</h2>
    <h2>学生性别：{{sex}}</h2>
    <h2>学生年龄：{{myAge}}</h2>
    <button @click="updateAge">点击修改收到的年龄</button>
  </div>
</template>

<script>
  export default {
    name:"Student",
    data() {
      return {
        msg:"我在尚硅谷学习Vue",
        myAge:this.age
      }
    },
    methods:{
      updateAge(){
        this.myAge++
      }
    },
    // 简单声明接收
    // props:["name", "sex", "age"]  

    // 接收的同时对数据进行类型限制
    /* props:{
      name:String,
      sex:String,
      age:Number
    } */

    // 接收的同时对数据：进行类型限制+默认值的指定+必要性的限制
    props:{
      name:{
        type:String,  // name的类型是字符串
        required:true,  // name是必要的
      },
      sex:{
        type:String,
        require:true,
      },
      age:{
        type:Number,
        default:99  // 默认值
      }
    }
  }
</script>